    private void txtPKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPKeyTyped
        char numero = evt.getKeyChar();
        if((numero<'0' || numero>'9') && (numero<',' || numero>'.')){
            evt.consume();
        }
        
        if (tasaPeriodica.isEnabled()) {
            jPanel4.setVisible(true);
        }
        if (numeroPeriodos.isEnabled()) {
            jPanel2.setVisible(true);
        }
        if (cuotaUniforme.isEnabled()) {
            jPanel5.setVisible(true);
        }
    }//GEN-LAST:event_txtPKeyTyped

